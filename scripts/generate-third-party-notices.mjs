/**
 * Regenerates THIRD-PARTY-NOTICES.md — the attribution file MIT / ISC / BSD /
 * OFL all require us to ship with the app.
 *
 *   node scripts/generate-third-party-notices.mjs
 *
 * The list is derived from the actual JS bundle, not from `npm ls`. Those are
 * very different sets: the npm production tree includes Expo's own CLI and
 * bundler (~450 packages, some of them MPL/LGPL), none of which are compiled
 * into the app. Metro only bundles what app code imports — 46 packages, all
 * permissive. Attributing the build toolchain would wrongly imply copyleft code
 * ships inside MIRI Alarm.
 *
 * So this exports a real bundle and reads its source map. It takes a minute;
 * run it at release time, or whenever dependencies change.
 */
import { execFileSync } from 'node:child_process';
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';

const OUT = 'THIRD-PARTY-NOTICES.md';
const PLATFORMS = ['android', 'ios'];
const LICENSE_FILE = /^(LICENSE|LICENCE|COPYING|NOTICE)(\.(md|txt))?$/i;
const COPYRIGHT_LINE = /^\s*(Copyright|\(c\)|©).*/gim;
/** Grabs `foo` or `@scope/foo` out of a node_modules path in a source map. */
const PACKAGE_IN_PATH = /node_modules\/((?:@[^/]+\/)?[^/]+)/;

const exportBundle = (outDir) => {
  execFileSync(
    'npx',
    [
      'expo',
      'export',
      ...PLATFORMS.flatMap((platform) => ['--platform', platform]),
      '--source-maps',
      '--output-dir',
      outDir,
    ],
    { stdio: 'inherit' },
  );
};

/** Every npm package with at least one module in a shipped bundle. */
const bundledPackages = (outDir) => {
  const found = new Set();
  for (const platform of PLATFORMS) {
    const dir = path.join(outDir, '_expo/static/js', platform);
    if (!fs.existsSync(dir)) continue;
    for (const file of fs.readdirSync(dir).filter((f) => f.endsWith('.map'))) {
      const { sources } = JSON.parse(fs.readFileSync(path.join(dir, file), 'utf8'));
      for (const source of sources) {
        const match = source.match(PACKAGE_IN_PATH);
        if (match) found.add(match[1]);
      }
    }
  }
  return [...found].sort();
};

/** The notice a permissive license actually obliges us to reproduce. */
const readCopyrights = (dir) => {
  const file = fs.readdirSync(dir).find((name) => LICENSE_FILE.test(name));
  if (!file) return [];
  const text = fs.readFileSync(path.join(dir, file), 'utf8');
  return [...new Set((text.match(COPYRIGHT_LINE) ?? []).map((line) => line.trim()))];
};

const describe = (name) => {
  const dir = path.join('node_modules', name);
  const manifest = JSON.parse(fs.readFileSync(path.join(dir, 'package.json'), 'utf8'));
  return {
    name,
    version: manifest.version,
    license: manifest.license ?? 'see package',
    copyrights: readCopyrights(dir),
  };
};

const tmp = fs.mkdtempSync(path.join(os.tmpdir(), 'miri-notices-'));
try {
  exportBundle(tmp);
  const packages = bundledPackages(tmp).map(describe);
  const licenses = [...new Set(packages.map((p) => p.license))].sort();
  const copyleft = packages.filter((p) => /GPL|MPL/i.test(p.license));

  if (copyleft.length) {
    throw new Error(
      `Copyleft license in the app bundle: ${copyleft.map((p) => `${p.name} (${p.license})`).join(', ')}. ` +
        `Review before shipping — this file assumes the bundle is permissive-only.`,
    );
  }

  const entry = (p) =>
    [
      `### ${p.name} ${p.version}`,
      ``,
      `License: ${p.license}`,
      ...(p.copyrights.length ? ['', ...p.copyrights.map((c) => `> ${c}`)] : []),
    ].join('\n');

  fs.writeFileSync(
    OUT,
    `# Third-Party Notices

MIRI Alarm bundles the open-source components listed below. Each is used under
its own license; the copyright notices reproduced here are what those licenses
require us to ship. MIRI Alarm itself is proprietary — see LICENSE.

Generated by \`scripts/generate-third-party-notices.mjs\` from the JS bundle
Metro actually produces. Do not edit by hand.

**${packages.length} packages**, all permissive: ${licenses.join(', ')}.

## Fonts

The bundled typefaces are licensed under the SIL Open Font License 1.1, which
permits embedding them in an application. Full text: https://openfontlicense.org

- **Pretendard** — Copyright (c) 2021 Kil Hyung-jin, with Reserved Font Name Pretendard.
- **Nunito** — Copyright (c) The Nunito Project Authors, with Reserved Font Name Nunito.

## Sounds

\`miri_fallback_alarm.wav\` is a synthesized tone, not a sampled recording, and
carries no third-party rights. Its structure is machine-generated throughout: a
1046.5 Hz (C6) band-limited square wave — odd harmonics only — in three 150 ms
beeps on an exact 250 ms period, with 8 ms linear fades, digital silence between
beeps, and no encoder metadata.

## Native dependencies

The native layer is not covered by the generated list above. Android links
AndroidX and the Kotlin stdlib (Apache-2.0) alongside React Native's native code
(MIT); iOS links the React Native and Expo pods (MIT). All are permissive and
attribution-only.

## Packages

${packages.map(entry).join('\n\n')}
`,
  );
  console.log(`${OUT}: ${packages.length} bundled packages, ${licenses.length} distinct licenses`);
} finally {
  fs.rmSync(tmp, { recursive: true, force: true });
}
